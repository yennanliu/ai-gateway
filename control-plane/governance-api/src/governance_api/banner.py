def show_banner(*, version: str, litellm_version: str, environment: str) -> None:
    """Print the AI Gateway startup banner + build info to the console."""
    info_lines = [
        f"AI Gateway v{version}  (data plane: LiteLLM {litellm_version})",
        f"environment: {environment}",
        "docs: http://localhost:8080/docs",
        "https://github.com/yennanliu/ai-gateway",
    ]
    width = max(len(line) for line in info_lines) + 4
    border = "#" + "-" * (width) + "#"

    box = [border, "#" + " " * width + "#"]
    for line in info_lines:
        box.append(f"#  {line.ljust(width - 2)}#")
    box.append("#" + " " * width + "#")
    box.append(border)

    print(AIGW_BANNER)  # noqa: T201
    print("\n".join(box) + "\n")  # noqa: T201
